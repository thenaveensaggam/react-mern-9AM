/**
 * for(initialization; condition; incr/decr){
 *     statements;
 * }
 */

/**
 * print from 0 to 10 diff 1
 */
let result = "";
for(let i=0; i<=10; i++){
   result += `${i} `;
}
console.log(result);

/**
 * print from 20 to 0 diff 2
 */
result = "";
for(let i=20; i>=0; i -= 2){
    result += `${i} `;
}
console.log(result);

/**
 * *
 * * *
 * * * *
 * * * * *
 * * * * * *
 */
result = "";
for(let i=1; i<=5; i++){
    for(let j=1; j<=i; j++){
        result += ` * `;
    }
    result += `\n`;
}
console.log(result);

